// 获取地图类型颜色
export const getMapTypeColor = (type: string): string => {
  const colors: Record<string, string> = {
    control: '#3b82f6',    // 蓝色
    hybrid: '#8b5cf6',     // 紫色
    escort: '#f59e0b',     // 橙色
    push: '#10b981',       // 绿色
    flashpoint: '#ec4899', // 粉色
  };
  return colors[type] || '#6b7280';
};
