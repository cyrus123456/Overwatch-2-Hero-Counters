interface I18nContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  // 首先检查 localStorage 是否有用户保存的语言设置
  const savedLanguage = typeof window !== 'undefined' ? localStorage.getItem('language') as Language | null : null;
  
  const [language, setLanguageState] = useState<Language>(savedLanguage || 'en');

  const setLanguage = useCallback((lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem('language', lang);
  }, []);

  useEffect(() => {
    // 如果用户没有保存语言设置，则自动检测
    if (!savedLanguage) {
      // 使用浏览器语言设置
      const browserLang = navigator.language || 'en';
      let detectedLanguage: Language = 'en';
      
      // 匹配浏览器语言到支持的语言
      if (browserLang.startsWith('zh')) {
        detectedLanguage = browserLang.includes('TW') || browserLang.includes('HK') ? 'zh-TW' : 'zh';
      } else if (browserLang.startsWith('ja')) {
        detectedLanguage = 'ja';
      } else if (browserLang.startsWith('ko')) {
        detectedLanguage = 'ko';
      } else if (browserLang.startsWith('es')) {
        detectedLanguage = 'es';
      } else if (browserLang.startsWith('fr')) {
        detectedLanguage = 'fr';
      } else if (browserLang.startsWith('de')) {
        detectedLanguage = 'de';
      } else if (browserLang.startsWith('pt')) {
        detectedLanguage = 'pt';
      } else if (browserLang.startsWith('ru')) {
        detectedLanguage = 'ru';
      } else if (browserLang.startsWith('it')) {
        detectedLanguage = 'it';
      }
      
      setLanguageState(detectedLanguage);
      localStorage.setItem('language', detectedLanguage);
    }
  }, [savedLanguage]);

  const t = useCallback(
    (key: string): string => {
      return translations[language][key] || key;
    },
    [language]
  );

  return React.createElement(
    I18nContext.Provider,
    { value: { language, setLanguage, t } },
    children
  );
}

export function useI18n(): I18nContextType {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within an I18nProvider');
  }
  return context;
}